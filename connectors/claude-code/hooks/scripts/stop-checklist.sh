#!/usr/bin/env bash
# Stop hook — lightweight session end checklist.
#
# Heavy work (validation, escalation checks) runs asynchronously
# via the daemon — it does NOT block session end.

set -euo pipefail

PROJECT_DIR="${CLAUDE_PROJECT_DIR:-.}"

# ─── Session State Safety Net ────────────────────────────────────────────────
# Write minimal session state if the orchestrator didn't maintain one.
SESSION_FILE="$PROJECT_DIR/tmp/session-state.md"
mkdir -p "$PROJECT_DIR/tmp"

WRITE_STATE=true
if [ -f "$SESSION_FILE" ]; then
  if grep -q "^### Steps\|^### Completed\|^### Task Progress" "$SESSION_FILE" 2>/dev/null; then
    WRITE_STATE=false
  elif find "$SESSION_FILE" -mmin -1 2>/dev/null | grep -q .; then
    WRITE_STATE=false
  fi
fi

if [ "$WRITE_STATE" = true ]; then
  RECENT_COMMITS=$(cd "$PROJECT_DIR" && git log --oneline -5 2>/dev/null || true)
  UNCOMMITTED=$(cd "$PROJECT_DIR" && git status --short 2>/dev/null | wc -l | tr -d ' ')
  cat > "$SESSION_FILE" << SESSIONEOF
## Session: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

### Recent Commits
${RECENT_COMMITS:-No commits}

### Uncommitted Changes
${UNCOMMITTED} files

### Notes
Auto-generated by stop hook. Orchestrator should maintain richer state.
SESSIONEOF
fi

# ─── Checklist (always shown) ────────────────────────────────────────────────
cat <<'EOF'
SESSION END:
1. Ensure session state is written to tmp/session-state.md
2. Commit all work
3. No uncommitted changes left behind
EOF

exit 0
