#!/usr/bin/env bash
# Stop hook — hard gate on uncommitted/unpushed work, soft warn on session state.
#
# Exit codes:
#   0 — session may end (warnings only)
#   2 — session BLOCKED (uncommitted or unpushed work)

set -euo pipefail

PROJECT_DIR="${CLAUDE_PROJECT_DIR:-.}"

# ─── Session State Safety Net ────────────────────────────────────────────────
# Write minimal session state if the orchestrator didn't maintain one.
SESSION_FILE="$PROJECT_DIR/tmp/session-state.md"
mkdir -p "$PROJECT_DIR/tmp"

WRITE_STATE=true
if [ -f "$SESSION_FILE" ]; then
  # Preserve orchestrator-maintained state: any structured section means don't overwrite
  if grep -qiE "^#+ *(Steps|Completed|Task Progress|Next Session|Priorities|Blockers|Current scope)" "$SESSION_FILE" 2>/dev/null; then
    WRITE_STATE=false
  # Also preserve if the file was NOT auto-generated by this hook
  elif ! grep -q "Auto-generated by stop hook" "$SESSION_FILE" 2>/dev/null; then
    WRITE_STATE=false
  fi
fi

if [ "$WRITE_STATE" = true ]; then
  RECENT_COMMITS=$(cd "$PROJECT_DIR" && git log --oneline -5 2>/dev/null || true)
  UNCOMMITTED_COUNT=$(cd "$PROJECT_DIR" && git status --short 2>/dev/null | wc -l | tr -d ' ')
  cat > "$SESSION_FILE" << SESSIONEOF
## Session: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

### Current Scope
Unknown — auto-generated by stop hook. Orchestrator should maintain this file.

### Steps
- Recent commits: ${RECENT_COMMITS:-No commits}
- Uncommitted changes: ${UNCOMMITTED_COUNT} files

### Decisions Made
None recorded.

### Next Session Priorities
1. Review git log and reconstruct what was done
2. Write proper session state

Auto-generated by stop hook. Orchestrator should maintain richer state.
SESSIONEOF
fi

# ─── Hard Gate: Uncommitted Files ─────────────────────────────────────────────
UNCOMMITTED_COUNT=0
if cd "$PROJECT_DIR" 2>/dev/null; then
  UNCOMMITTED_COUNT=$(git status --short 2>/dev/null | wc -l | tr -d ' ')
fi

if [ "$UNCOMMITTED_COUNT" -gt 0 ]; then
  MSG="Cannot end session: ${UNCOMMITTED_COUNT} uncommitted file(s). Commit and push before stopping.\\n\\nRun 'git add' and 'git commit' for all outstanding changes, then 'git push'."
  printf '{"systemMessage":"%s"}' "$MSG"
  exit 2
fi

# ─── Hard Gate: Unpushed Commits ──────────────────────────────────────────────
UNPUSHED_COUNT=0
if cd "$PROJECT_DIR" 2>/dev/null; then
  UPSTREAM=$(git rev-parse --abbrev-ref '@{upstream}' 2>/dev/null || true)
  if [ -n "$UPSTREAM" ]; then
    UNPUSHED_COUNT=$(git rev-list "$UPSTREAM"..HEAD --count 2>/dev/null || echo "0")
  fi
fi

if [ "$UNPUSHED_COUNT" -gt 0 ]; then
  MSG="Cannot end session: ${UNPUSHED_COUNT} unpushed commit(s). Run 'git push' before stopping."
  printf '{"systemMessage":"%s"}' "$MSG"
  exit 2
fi

# ─── Soft Warning: Next Session Priorities ────────────────────────────────────
PRIORITIES_MISSING=true
if [ -f "$SESSION_FILE" ]; then
  if grep -qiE "^#+ *Next Session Priorities|^#+ *Next Session|^#+ *Next " "$SESSION_FILE" 2>/dev/null; then
    PRIORITIES_MISSING=false
  fi
fi

CHECKLIST="SESSION END: All work committed and pushed. Session may end cleanly."

WARNINGS=""
if [ "$PRIORITIES_MISSING" = true ]; then
  WARNINGS="WARNING: No next session priorities found in tmp/session-state.md. Consider adding a '## Next Session Priorities' section for the next session."
fi

if [ -n "$WARNINGS" ]; then
  MSG="${WARNINGS}\\n\\n${CHECKLIST}"
  printf '{"systemMessage":"%s"}' "$MSG"
else
  printf '{"systemMessage":"%s"}' "$CHECKLIST"
fi

exit 0
